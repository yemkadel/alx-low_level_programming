#include "main.h"
/**
 * add - add
 *
 * @a: a
 * @b: b
 * Return: int
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - sub
 *
 * @a: a
 * @b: b
 * Return: int
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - mul
 *
 * @a: a
 * @b: b
 * Return: int
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - div
 *
 * @a: a
 * @b: b
 * Return: int
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod
 *
 * @a: a
 * @b: b
 * Return: int
 */
int mod(int a, int b)
{
	return (a % b);
}
